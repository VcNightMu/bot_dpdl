# 标准名集合（快速查找）
_CATEGORY_SET: set[str] = set(CATEGORIES)


def normalize_category(user_input: str) -> str | None:
    """把用户输入的流派名归一化为标准名

    返回标准名或 None（未找到）
    """
    if not user_input or not user_input.strip():
        return None

    stripped = user_input.strip()

    # 1. 直接匹配标准名
    if stripped in _CATEGORY_SET:
        return stripped

    # 2. 查别名
    return CATEGORY_ALIASES.get(stripped)
